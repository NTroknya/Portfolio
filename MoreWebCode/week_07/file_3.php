    <h3>M-V-C: (Model-View-Controller)</h3>
    <ul>
        <li>Keep presentation of data seperated from the program that interact with data</li>
        <li>Allows front-end developers and back-end developers to work on the same system without interfering</li>
    </ul>
    <ul>
        <li>Model: Responsible for the following:
            <ol>
                <li>Manage Data</li>
                <li>Store Data</li>
                <li>Retrives Data</li>
            </ol>
        </li>
        <li>View: Presentation Layer. Responsible to display data. After formating this data that will be presented to the client</li>
        <li>Controller: Enables the Model and View to work together</li>
    </ul>
